package dataStructure;

public class src_dest {
	private int src;
	private int dest;

	public src_dest(int src, int dest) {
		this.src = src;
		this.dest = dest;
	}

	public int getSrc() {
		return this.src;
	}

	public int getDest() {
		return this.dest;
	}

	public void setSrc(int src) {
		this.src = src;
	}

	public void setDest(int dest) {
		this.dest = dest;
	}
}
